        function plot(clr) 
            
           assert(isa(clr,'Color'));
           
           ncols = 12; nrows = 12;
           
           len = clr.c.Count;
           keySet = keys(clr.c);
           
           [x,y] = meshgrid(0:5:5*ncols, nrows:-1:0);
           
           if strcmp(clr.lang,'english')
               wTitle = 'Available colors in Color class';
           elseif strcmp(clr.lang,'francais')
               wTitle = 'Couleurs disponibles de la classe Color';
           elseif strcmp(clr.lang, 'espanol')
               wTitle = 'Colores disponibles en la clase Color';
           end
           
           figure('Name',wTitle, ...
                  'NumberTitle','off', ...
                  'units', 'normalized', ...
                  'outerposition', [ 0 0 1 1 ]),
                  %'MenuBar','none' ...
                  
           hold on,
           cmap = rgb(clr, keySet);
           colormap(cmap);
           
           rect  = zeros(4,1,3);
           set(gca,'CLim',[1 len]);
           
           % Maroon color
           if     strcmp(clr.lang,'english'),  mr = clr.c('maroon'); 
           elseif strcmp(clr.lang,'francais'), mr = clr.c('marron'); 
           elseif strcmp(clr.lang,'espanol'),  mr = clr.c('marrón'); end;
           
           for i=1:nrows
               
               for j=1:ncols
                   nr = ncols*(i-1) + j; 
                   if nr>len, break; end
                   
                   rect(:,1,:) = [x(i,j)     y(i,j)     0; ...
                                  x(i+1,j)   y(i+1,j)   0; ...
                                  x(i+1,j+1) y(i+1,j+1) 0; ...
                                  x(i,j+1)   y(i,j+1)   0];
                   
                   patch(rect(:,:,1), rect(:,:,2), rect(:,:,3), ...
                         'FaceColor','flat','CData',nr, ...
                         'CDataMapping','direct');
                     
                   xt = (x(i,j) + x(i,j+1)) / 2;
                   yt = (y(i,j) + y(i+1,j)) / 2; yt = yt-1;
                   text(xt,yt,keySet{nr}, 'FontSize', 8, ...
                        'Color', mr, ...
                        'HorizontalAlignment', 'Center');
               end
               
               y = y - 2*ones(nrows+1,ncols+1);
           end
           
           axis off, axis equal, axis tight, 
           hold off,

        end
        
