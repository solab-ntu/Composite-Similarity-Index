        function [rgb_t, error] = rgb(clr, color_name)
           
            assert(isa(clr,'Color'));
            assert(isa(color_name,'char') || iscellstr(color_name));
            
            error = 0;
            
            if isa(color_name,'char')
                
                if strcmp(clr.lang,'english')
                    % remove all blanks and transform to lowercase
                    color_name(ismember(color_name, ' ')) = [];
                    color_name = lower(color_name);
                end
                
                if any(strcmp(color_name,keys(clr.c)))
                    rgb_t = clr.c(color_name);
                else
                    rgb_t = []; error = 1;
                end
                
            else
                
                len = length(color_name);
                rgb_t = [];
                for i=1:len
                    curr_name = char(color_name{i});
                    if strcmp(clr.lang,'english')
                        curr_name(ismember(curr_name, ' ')) = [];
                        curr_name = lower(curr_name);
                    end
                    if any(strcmp(curr_name,keys(clr.c)))
                        rgb_t = [rgb_t; clr.c(curr_name)];
                    else
                        error = 1;
                    end
                end
            end
            
        end
        
