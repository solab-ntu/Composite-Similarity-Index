    % ans =
    %     0.9608    0.9608    0.8627
    %     0.5020         0         0
    %     1.0000    0.9608    0.9333
    %     1.0000    0.0784    0.5765
    %
    % (5) plot the sine function with a 'deep pink' color:
    % plot(linspace(0,2*pi,100),sin(linspace(0,2*pi,100)), ...
